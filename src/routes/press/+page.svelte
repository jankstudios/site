    <h5>JANK UNVEILS “ICE POP” — A SATIRICAL ART OBJECT FROZEN IN A 1,000 LB ICE BLOCK IN WASHINGTON SQUARE PARK</h5>
    <br>
    <p><strong>New York, NY — March 9, 2026</strong> — On the first warm, sunny day of the year, a 1,000-pound block of ice appeared in Washington Square Park. Measuring approximately 24 x 24 x 48 inches, the ice contained nine boxed objects frozen inside. No signage. No explanation.</p>

    <p>Within minutes, a crowd formed.</p>

    <p>A hammer—chained to the ice—invited participation. Passersby began striking the block. The scene escalated: strangers took turns smashing, kicking, and chipping away. At one point, a man played saxophone while breaking the ice. Within an hour, the structure was completely destroyed.</p>

    <p>Inside were ICE Pops.</p>
    <br>
    <h5>WHAT IS ICE POP?</h5>
    <br>
    <p>ICE Pop is a satirical anti-ICE art object created by NJ/NYC-based creative studio Jank, a collective working at the intersection of art, commerce, and provocation.</p>

    <p>Packaged to resemble a brightly colored frozen treat, each ICE Pop contains a set of interlocking steel spike tabs housed inside a custom paperboard carton. The packaging includes a parody nutrition label featuring ICE-related statistics, as well as a popsicle stick printed with ICE-related jokes.</p>

    <p>The project blends humor, discomfort, and materiality to challenge perceptions of authority, power, and complicity.</p>

    <p>“Humor often cuts deeper and speaks more truth,” said Jank. “ICE Pop is about confronting uncomfortable realities in a way that’s immediate, absurd, and impossible to ignore.”</p>
    <br>
    <h5>A PARTICIPATORY SCULPTURE</h5>
    <br>
    <p>The Washington Square Park installation was designed as a public intervention.</p>

    <p>By freezing ICE Pops inside a massive ice block and inviting strangers to break it apart, the work transformed spectators into participants. The act of “smashing ice” became both literal and symbolic—an unscripted performance shaped entirely by the crowd.</p>

    <p>The location was intentional: the installation appeared at the same site as a recent immigration-related protest, layering the work into an ongoing public conversation.</p>

    <p>No press was notified in advance.</p>
    <br>
    <h5>ART OBJECT, NOT INSTRUCTION</h5>
    <br>
    <p>ICE Pop exists as an art object. While it contains real materials that could theoretically be assembled into functional spikes, Jank explicitly discourages use.</p>

    <p>“ICE Pop is about the idea of action, not the execution,” the collective states. “It’s a display piece, a conversation starter, and a provocation.”</p>

    <p>Each unit includes multiple components—steel tabs, rubber bands, packaging elements—all sourced and manufactured in the United States, with final assembly in New Jersey. 100% of ICE Pop profits support the National Immigrant Justice Center.</p>
    <br>
    <h5>A PRODUCT THAT BLURS CATEGORIES</h5>
    <br>
    <p>ICE Pop occupies multiple roles simultaneously: an art object, a political statement, a piece of satire, a consumer product, and a public stunt.</p>

    <p>It is currently available for purchase starting at $19.99 via Jank’s website through May 1, 2026.</p>

    <p>Jank describes the project as intentionally ambiguous—designed to provoke reactions ranging from empowerment and humor to discomfort and critique.</p>
    <br>
    <h5>ANTICIPATED REACTION</h5>
    <br>
    <p>The collective acknowledges the project’s controversial nature.</p>

    <p>Critics may view ICE Pop as dangerous or irresponsible. Jank agrees that the object carries real-world implications—but argues that focusing solely on its literal function misses the point.</p>

    <p>“The most common misunderstanding is treating ICE Pop as something to be used,” they said. “It’s an art piece. The tension comes from the fact that it could be used—but isn’t meant to be.”</p>
